namespace MathfClass
{
    public class MathClass
    {

        public int SumNumber(int value1, int value2)
        {
            return value1 + value2;
        }

        public int MultiplicationNumber(int value1, int value2)
        {
            return value1 * value2;
        }

        public static int DifferenceNumber(int value1, int value2)
        {
            return value1 - value2;
        }
    }
}